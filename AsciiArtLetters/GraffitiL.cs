				@"_____   ",
				@"\__  \  ",
				@" / __ \_",
				@"(____  /",
				@"     \/ "
			};

            // turn the array to a string using a for loop
			for (int i = 0; i < letterarray.Length; i++)
			{
				letterstr += letterarray[i] + "\n";
			}

			// return the final string
			return letterstr;
		}


